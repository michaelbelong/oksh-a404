export function transform(text, map = monospaceCharMap) {
  let out = "";
  for (const char of text.split("")) {
    if (map[char] !== undefined) out += map[char];
    else if (map[char.toLowerCase()] !== undefined)
      out += map[char.toLowerCase()];
    else out += char;
  }
  return out;
}

